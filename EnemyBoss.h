#ifndef ENEMY_BOSS_H
#define ENEMY_BOSS_H

#include "Enemy.h"

//==============================================================================
// ボス型エネミークラス
//
// 特徴
// ・HP 8000（通常の約27倍）
// ・視野距離 20m（全エネミー中最大）
// ・モデルスケール 0.9f
// ・左右バレル2門からプレイヤーへ射撃（1秒間隔、弾速4.0f、ダメージ24）
//==============================================================================
class EnemyBoss : public Enemy
{
public:
    static constexpr int   HP         = 8000;   // ボスHP
    static constexpr float BOSS_SIZE  = 0.9f;   // モデルスケール
    static constexpr float SIGHT_DIST = 20.0f;  // 視野距離
    static constexpr float CHASE_SPD  = 4.5f;   // 追跡速度（m/s）
    static constexpr float PATROL_SPD = 2.0f;   // 巡回速度（m/s）

    // 射撃パラメータ
    static constexpr float SHOOT_INTERVAL    = 1.0f;   // 発射間隔（秒）
    static constexpr int   SHOOT_DAMAGE      = 24;     // 1発あたりのダメージ
    static constexpr float BOSS_BULLET_SPEED = 4.0f;   // 弾速（遅め）
    static constexpr float BARREL_OFFSET     = 0.4f;   // バレル左右オフセット（プレイヤー BARREL_SIDE_X=0.40f 準拠）
    static constexpr float BARREL_FORWARD    = 0.5f;   // バレル前方オフセット（ボディ前面に出す）

    //==========================================================================
    // 初期化処理
    //==========================================================================
    void Initialize(const DirectX::XMFLOAT3& position) override;

    //==========================================================================
    // 終了処理（SEのアンロード）
    //==========================================================================
    void Finalize() override;

    //==========================================================================
    // 更新処理（基底AI＋射撃タイマー）
    //==========================================================================
    void Update(double elapsed_time) override;

private:
    float m_shootTimer = 0.0f;  // 射撃インターバルタイマー
    int   m_shootSE    = -1;    // ショットガンSEハンドル

    //==========================================================================
    // 射撃処理（左右バレルから各1発）
    //==========================================================================
    void Shoot();
};

#endif // ENEMY_BOSS_H
