#include "key_logger.h"
#include "keyboard.h"

// -----------------------------------------------------------------------------
// グローバル変数
// -----------------------------------------------------------------------------
static Keyboard_State g_PrevState{};      // 前フレームのキー状態
static Keyboard_State g_TriggerState{};   // トリガー判定用（押した瞬間）
static Keyboard_State g_ReleaseState{};   // リリース判定用（離した瞬間）

//==============================================================================
// 初期化
//==============================================================================
void KeyLogger_Initialaize()
{
    Keyboard_Initialize();
}

//==============================================================================
// 更新処理：前フレームと現フレームの差分からトリガー・リリースを判定
//==============================================================================
void KeyLogger_Update()
{
    const Keyboard_State* pState = Keyboard_GetState();

    LPBYTE pn = (LPBYTE)pState;
    LPBYTE pp = (LPBYTE)&g_PrevState;
    LPBYTE pt = (LPBYTE)&g_TriggerState;
    LPBYTE pr = (LPBYTE)&g_ReleaseState;

    for (int i = 0; i < sizeof(Keyboard_State); i++)
    {
        // トリガー判定：前フレーム OFF かつ 今フレーム ON
        pt[i] = (pp[i] ^ pn[i]) & pn[i];

        // リリース判定：前フレーム ON かつ 今フレーム OFF
        pr[i] = (pp[i] ^ pn[i]) & pp[i];
    }

    // 現在の状態を前フレームとして保存
    g_PrevState = *pState;
}

//==============================================================================
// 押下判定：キーが押されている間ずっと true
//==============================================================================
bool KeyLogger_IsPressed(Keyboard_Keys key)
{
    return Keyboard_IsKeyDown(key);
}

//==============================================================================
// トリガー判定：キーが押された瞬間のみ true
//==============================================================================
bool KeyLogger_IsTrigger(Keyboard_Keys key)
{
    return Keyboard_IsKeyDown(key, &g_TriggerState);
}

//==============================================================================
// リリース判定：キーが離された瞬間のみ true
//==============================================================================
bool KeyLogger_IsRelease(Keyboard_Keys key)
{
    return Keyboard_IsKeyDown(key, &g_ReleaseState);
}
