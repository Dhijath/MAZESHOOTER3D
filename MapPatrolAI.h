#ifndef MAP_PATROL_AI_H
#define MAP_PATROL_AI_H

#include <DirectXMath.h>
#include <vector>

//==============================================================================
// 床座標リスト初期化
//
// ■役割
// ・マップ生成時に呼び出され、すべての床座標を保存する
// ・Map_GenerateDungeon() の最後で1回だけ呼ぶ
//
// ■引数
// ・floorPositions : 床のワールド座標リスト
//==============================================================================
void MapPatrolAI_Initialize(const std::vector<DirectX::XMFLOAT3>& floorPositions);

//==============================================================================
// ランダムな巡回目的地を取得
//
// ■役割
// ・床座標リストからランダムに1つ選んで返す
// ・エネミーが目的地に到達したら、この関数で新しい目的地を取得する
//
// ■戻り値
// ・床上のランダムなワールド座標
// ・床がない場合は (0,1,0) を返す
//==============================================================================
DirectX::XMFLOAT3 MapPatrolAI_GetRandomDestination();

//==============================================================================
// 床座標リストのクリア
//
// ■役割
// ・マップ再生成時に古い床座標をクリアする
//==============================================================================
void MapPatrolAI_Clear();

//==============================================================================
// 視線遮蔽判定
//
// ■役割
// ・from から to の間に壁があるか判定する
// ・壁があれば「見えていない」と判断する
//
// ■引数
// ・from : 視線の始点（敵の位置）
// ・to   : 視線の終点（プレイヤーの位置）
//
// ■戻り値
// ・true  : 壁がなく見えている
// ・false : 壁に遮られて見えていない
//==============================================================================
bool MapPatrolAI_HasLineOfSight(
    const DirectX::XMFLOAT3& from,
    const DirectX::XMFLOAT3& to);

//==============================================================================
// 到達可能な巡回目的地を取得
//
// ■役割
// ・ランダムに目的地を選び、始点から直線で行けるか確認する
// ・壁に遮られている場合は引き直す（最大試行回数あり）
//
// ■引数
// ・from     : 現在地（敵の位置）
// ・maxTry   : 最大試行回数
//
// ■戻り値
// ・到達可能な床座標（試行回数内で見つからなければ完全ランダムで返す）
//==============================================================================
DirectX::XMFLOAT3 MapPatrolAI_GetReachableDestination(
    const DirectX::XMFLOAT3& from,
    int maxTry = 10);
//==============================================================================
// 近距離の巡回目的地を取得
//
// ■役割
// ・現在位置から searchRadius 以内の床座標を候補にする
// ・候補の中から視線が通る座標を返す
// ・見つからない場合は GetReachableDestination にフォールバックする
//
// ■引数
// ・from         : 現在地（敵の位置）
// ・searchRadius : 探索半径（メートル）
// ・maxTry       : 最大試行回数
//
// ■戻り値
// ・近距離の到達可能な床座標
//==============================================================================
DirectX::XMFLOAT3 MapPatrolAI_GetNearbyDestination(
    const DirectX::XMFLOAT3& from,
    float searchRadius = 3.0f,
    int maxTry = 10);

#endif // MAP_PATROL_AI_H