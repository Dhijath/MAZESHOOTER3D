#ifndef ENEMY_AI_H
#define ENEMY_AI_H

#include <DirectXMath.h>

//==============================================================================
// AI更新処理（追跡 + 巡回）
//
// 機能説明
// ・プレイヤーが視野内 → 追跡モード
// ・プレイヤーが視野外 → 巡回モード（目的地へ移動）
// ・目的地に到達で新しい目的地をランダム設定
//
// 引数
// ・ioPosition    : エネミーの現在位置（入出力）
// ・ioVelocity    : エネミーの現在速度（入出力・移動量）
// ・ioFront       : エネミーの現在向き（入出力・回転）
// ・ioDestination : 現在目的地（入出力・到達で自動更新）
// ・ioWasChasing  : 前フレームの追跡フラグ（入出力）
// ・dt            : 経過時間（秒）
// ・chaseSpeed    : 追跡時の移動速度（メートル/秒）
// ・patrolSpeed   : 巡回時の移動速度（メートル/秒）
// ・sightDistance : 視野距離（メートル）
//==============================================================================
void EnemyAI_Update(
    DirectX::XMFLOAT3* ioPosition,
    DirectX::XMFLOAT3* ioVelocity,
    DirectX::XMFLOAT3* ioFront,
    DirectX::XMFLOAT3* ioDestination,
    bool* ioWasChasing,
    float              dt,
    float              chaseSpeed,
    float              patrolSpeed,
    float              sightDistance);

//==============================================================================
// 回転速度設定
//
// 機能
// ・向き変更時の回転速度を変更する（全エネミー共通）
//
// 引数
// ・degreesPerSecond : 回転速度（度/秒）
//==============================================================================
void EnemyAI_SetRotationSpeed(float degreesPerSecond);

//==============================================================================
// 到達判定距離設定
//
// 機能
// ・目的地に「到達した」と判定する距離を変更する（全エネミー共通）
//
// 引数
// ・distance : 到達判定距離（メートル）
//==============================================================================
void EnemyAI_SetArrivalDistance(float distance);

#endif // ENEMY_AI_H
